###Comparison operator / Relational Opearator

#1. == (Exact equal to)
print(20==20)
print(3=='3')

#2. != (not equal to)
print(6 != 7)

#3. > (Greater than)
print(56 > 89)
print(76>6)

#4. >= (Greater than equal to)
print(56 >= 56)

#5.  < (less than)
print(67<90)

#6.  <= (less than or equal to)
print(67<=80)
print(20<10)
