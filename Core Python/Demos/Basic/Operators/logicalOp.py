####Logical operator

#1.  and
# If both opearnds are True then return True otherwise False.
print(True and True)
print(True and False)

#2. or
# If both opearnds are False then return False otherwise True.
print(True or False)
print(False or False)

#3. not
# Reverse the result.
print(not True)
print(not False)

print(not 5)