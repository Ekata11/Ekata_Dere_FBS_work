### Assignment operator : Used to assign the value for variable .

x = 16

#1. +=
x += 13           
print(x)             
# x = x + 13
# x = 16 + 13
# x = 29

#2. -=
x = 16
x -= 13       
# x = x - 13    
print(x)

#3. /=
x = 28
x /= 14            
print(x)

#4. *=
x = 25
x *= 46
print(x)

#5. //=
x = 47
x //= 26
print(x)

#6. %=
x = 47
x %= 26
print(x)

#8. **=
x = 4
x **= 2
print(x)



